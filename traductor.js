let palabra = "coco" ;

switch (palabra) {
    case "perro":
        console.log("Dog");
        break;
    case "gato":
        console.log("Cat");
    break;
    
    case "puerta":
        console.log("Door");
    break;

    case "ventana":
        console.log("window");
    break;           

    case "mesa":
        console.log("table");
    default:
        console.log("La palabra ingresada es incorrecta");
        break;
}